function markQuestion(li, ok, answer, forceShowAns=false){
  li.dataset.done = '1';
  li.dataset.correct = ok ? '1' : '0';

  const msg = li.querySelector('.q-msg');
  if(!msg) return;

  if(ok){
    msg.innerHTML = '‚úÖ Ê≠£Á¢∫';
    msg.style.color = '#5bd3c7';
    // Ê≠£Á¢∫‰∏çÈ°ØÁ§∫Ê≠£Ëß£ÂÖßÂÆπ
    const ansLine = li.querySelector('.q-ans');
    if(ansLine) ansLine.textContent = 'Ê≠£Ëß£Ôºö';
  }else{
    msg.innerHTML = '‚ùå ÈåØË™§';
    msg.style.color = '#ff6b6b';
    // ÈåØË™§Ë¶ÅÈ°ØÁ§∫Ê≠£Ëß£
    const ansLine = li.querySelector('.q-ans');
    if(ansLine) ansLine.textContent = `Ê≠£Ëß£Ôºö ${answer}`;
  }

  // Ëã•ÊòØ„ÄåÈ°ØÁ§∫Á≠îÊ°à„ÄçÊ®°ÂºèÔºåÁÑ°Ë´ñÂ∞çÈåØÈÉΩÂç∞Ê≠£Ëß£
  if(forceShowAns){
    const ansLine = li.querySelector('.q-ans');
    if(ansLine) ansLine.textContent = `Ê≠£Ëß£Ôºö ${answer}`;
  }
}

