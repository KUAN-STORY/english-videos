async function loadQuizJSON(slug){
  try{
    const r = await fetch(`./data/quiz-${slug}.json`, {cache:'no-store'});
    if(!r.ok) return [];
    const raw = await r.json();
    return (raw || []).map(normalizeQuestion);
  }catch(e){
    console.error('[quiz] load error:', e);
    return [];
  }
}

